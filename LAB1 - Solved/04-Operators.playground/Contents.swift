/**
 Operators
 */
let firstNumber = 9;
let secondNumber = 2;

// Perform Addition
let addition: Int
addition = firstNumber + secondNumber
print("sum is \(addition)")
// Perform Substraction
let subtraction: Int
subtraction = firstNumber - secondNumber
print("sub is \(subtraction)")
// Perform Multiplication
let multiplication: Int
multiplication = firstNumber * secondNumber
print("mult is \(multiplication)")
// Perform Division
let division: Int
division = firstNumber / secondNumber
print("div is \(division)")
// Perform Remainder
let remainder: Int
remainder = firstNumber % secondNumber
print("rem is \(remainder)")
